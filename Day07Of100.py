#Main.py
import random
import hangman_art
import hangman_words
from replit import clear

display = []
word_length = 0
word_completed = False 
lives = len(hangman_art.stages)

chosen_word = random.choice(hangman_words.word_list)
word_length = len(chosen_word)
for i in range(0, word_length):
  display.append("_")

print(hangman_art.logo)
print(f"Word to Guess is: {display}")
print(chosen_word)

while not word_completed and lives > 0:
  guess = str(input("Please guess a letter: ")).lower()
  clear()
  
  for position in range(0, word_length):
    if chosen_word[position] == guess :
      if guess in display:
        print(f"You have already guessed [{guess}], try guessing another letter")
      else:
        display[position] = guess
  if guess not in chosen_word:
    print(hangman_art.stages[lives -1])
    lives -= 1
    print(f"You Guessed Letter [{guess}] but its not in the word and you are left with {lives} lifes")

  print(f"Word to Guess is: {display}")
  if "_" not in display:
    word_completed = True

if lives <= 0:
  print("YOU LOSE !!")
else:  
  print("YOU WIN !!")

