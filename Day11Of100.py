def deal_card():
  cards = [11, 2, 3, 4, 5, 6, 7, 8, 9, 10, 10, 10, 10]
  card = random.choice(cards)
  return card

def calculate_score(player):
  score = sum(player)
  if score == 21 and len(player) == 2:
    return 0

  if 11 in player and score > 21:
    player.remove(11)
    player.append(1)

  return score

